type Anchor = 'foot' | 'center' | 'topleft'

/** Draw a pixel grid. px = size of one pixel. Rows may differ in length (padded transparent). */
export function drawGrid(
  ctx: CanvasRenderingContext2D, rows: string[], x: number, y: number, px: number,
  anchor: Anchor = 'foot', pal?: Record<string, string>,
) {
  const w = rows.reduce((m, r) => Math.max(m, r.length), 0)
  const h = rows.length
  const cx = anchor === 'topleft' ? x : x - (w * px) / 2
  const cy = anchor === 'foot' ? y - h * px : anchor === 'center' ? y - (h * px) / 2 : y
  const ox = Math.round(cx)
  const oy = Math.round(cy)
  for (let gy = 0; gy < h; gy++) {
    const row = rows[gy]
    for (let gx = 0; gx < row.length; gx++) {
      const ch = row[gx]
      if (ch === '.' || ch === ' ') continue
      const col = (pal && pal[ch]) || CH[ch]
      if (!col) continue
      ctx.fillStyle = col
      // tiny overlap so fractional camera zoom leaves no seams between pixels
      ctx.fillRect(ox + gx * px, oy + gy * px, px + 0.5, px + 0.5)
    }
  }
}

