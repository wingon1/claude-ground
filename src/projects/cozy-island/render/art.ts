/** Render an icon to its own offscreen canvas (for crisp UI use). */
export function iconDataURL(name: string, size = 32): string {
  const rows = ICONS[name]
  if (!rows) return ''
  const c = document.createElement('canvas')
  c.width = size; c.height = size
  const ctx = c.getContext('2d')!
  const w = rows.reduce((m, r) => Math.max(m, r.length), 0)
  const px = Math.max(1, Math.floor(size / Math.max(w, rows.length)))
  const gw = w * px, gh = rows.length * px
  drawGrid(ctx, rows, (size - gw) / 2, (size - gh) / 2, px, 'topleft')
  return c.toDataURL()
}
